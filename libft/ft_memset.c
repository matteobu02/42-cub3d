#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*tmp;

	tmp = s;
	i = 0;
	while (tmp[i] && i < n)
		tmp[i++] = c;
	return (s);
}
